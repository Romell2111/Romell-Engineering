    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed

        if(organizationJComboBox.getSelectedItem() == null || employeeJComboBox.getSelectedItem()== null || roleJComboBox.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null, "Please create Organization, then the Employee and then assign Role to them", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if(ValidationMethods.isStringValid(nameJTextField.getText())== false){
            JOptionPane.showMessageDialog(null, "Please provide valid User Name", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String userName = nameJTextField.getText();

        for(UserAccount ua : EcoSystem.getInstance().getUserAccountDirectory().getUserAccountList()){
            if(ua.getUsername().equalsIgnoreCase(userName)){
                JOptionPane.showMessageDialog(null, "Username already exists", "Info", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        if(ValidationMethods.isStringValid(passwordJTextField.getText())== false){
            JOptionPane.showMessageDialog(null, "Please provide valid Password", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String password = passwordJTextField.getText();

       

            EmployeeOrganization organization = (EmployeeOrganization) organizationJComboBox.getSelectedItem();
            Employee employee = (Employee) employeeJComboBox.getSelectedItem();
            Role role = (Role) roleJComboBox.getSelectedItem();

            organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
            //  EcoSystem.getInstance().getUserAccountDirectory().createUserAccount(userName, password, employee, role);

            popData();
        
    }//GEN-LAST:event_createUserJButtonActionPerformed

