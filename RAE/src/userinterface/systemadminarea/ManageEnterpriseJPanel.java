private boolean checkEnterpriseNameExists(String name){
    boolean checkEnterpriseName =true;
        for(Network network: system.getNetworkList()){
            for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList()){
        if(name.equals(enterprise.getName())){
            checkEnterpriseName = false;
        JOptionPane.showMessageDialog(null, "Please enter new Enterprise Name","Message",JOptionPane.WARNING_MESSAGE);
          
        nameJTextField.setText("");
        }
        }
    
        }
        return checkEnterpriseName;
}
